require('dotenv').config();
const express = require('express');
const cors = require('cors');
const db = require('./models');
const { authMiddleware } = require('./users/authMiddleware');
const departmentRoutes = require('./department/departmentRoutes');
const userRoutes = require('./users/userRoutes');

const app = express();
const PORT = process.env.PORT || 3001;

// ĞšĞ¾Ğ½Ñ„Ğ¸Ğ³ÑƒÑ€Ğ°Ñ†Ğ¸Ñ CORS
const corsOptions = {
  origin: [
    'http://localhost:5173',
    'http://127.0.0.1:5173',
  ],
  methods: ['GET', 'POST', 'PUT', 'DELETE', 'PATCH', 'OPTIONS'],
  allowedHeaders: ['Content-Type', 'Authorization', 'X-Requested-With'],
  credentials: true,
  optionsSuccessStatus: 200,
  preflightContinue: false
};

// Middleware
app.use(cors(corsOptions));
app.options('*', cors(corsOptions));
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Ğ›Ğ¾Ğ³Ğ¸Ñ€Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ Ğ·Ğ°Ğ¿Ñ€Ğ¾ÑĞ¾Ğ²
app.use((req, res, next) => {
  console.log(`[${new Date().toISOString()}] ${req.method} ${req.path}`);
  next();
});

// Ğ˜Ğ½Ğ¸Ñ†Ğ¸Ğ°Ğ»Ğ¸Ğ·Ğ°Ñ†Ğ¸Ñ Ğ±Ğ°Ğ·Ñ‹ Ğ´Ğ°Ğ½Ğ½Ñ‹Ñ…
async function initializeDatabase() {
  try {
    await db.sequelize.authenticate();
    console.log('âœ… Database connection established');

    if (process.env.NODE_ENV === 'development') {
      await db.sequelize.sync({ alter: true });
      console.log('ğŸ”„ Database models synced');
    }
  } catch (err) {
    console.error('âŒ Database initialization error:', err);
    process.exit(1);
  }
}

// ĞœĞ°Ñ€ÑˆÑ€ÑƒÑ‚Ñ‹ API
app.use('/api/users', userRoutes);
app.use('/api/departments', departmentRoutes);

// ĞŸÑ€Ğ¾Ğ²ĞµÑ€ĞºĞ° Ğ°Ğ²Ñ‚Ğ¾Ñ€Ğ¸Ğ·Ğ°Ñ†Ğ¸Ğ¸
app.get('/api/check-auth', authMiddleware, (req, res) => {
  res.json({ 
    authenticated: true,
    user: req.user 
  });
});

// ĞŸÑ€Ğ¾Ğ²ĞµÑ€ĞºĞ° Ñ€Ğ°Ğ±Ğ¾Ñ‚Ñ‹ ÑĞµÑ€Ğ²ĞµÑ€Ğ°
app.get('/', (req, res) => {
  res.json({ 
    status: 'running',
    environment: process.env.NODE_ENV || 'development',
    timestamp: new Date().toISOString(),
    database: db.sequelize.config.database
  });
});

// ĞĞ±Ñ€Ğ°Ğ±Ğ¾Ñ‚ĞºĞ° 404
app.use((req, res) => {
  res.status(404).json({ 
    error: 'Not Found',
    path: req.path,
    method: req.method
  });
});

// ĞĞ±Ñ€Ğ°Ğ±Ğ¾Ñ‚ĞºĞ° Ğ¾ÑˆĞ¸Ğ±Ğ¾Ğº
app.use((err, req, res, next) => {
  console.error('ğŸ”¥ Server error:', err.stack);
  res.status(500).json({ 
    error: 'Internal Server Error',
    message: process.env.NODE_ENV === 'development' ? err.message : 'Something went wrong'
  });
});

// Ğ—Ğ°Ğ¿ÑƒÑĞº ÑĞµÑ€Ğ²ĞµÑ€Ğ°
initializeDatabase().then(() => {
  app.listen(PORT, () => {
    console.log(`ğŸš€ Server running on port ${PORT}`);
    console.log(`ğŸŒ CORS enabled for: ${corsOptions.origin.join(', ')}`);
    console.log(`ğŸ› ï¸ Environment: ${process.env.NODE_ENV || 'development'}`);
  });
}).catch(err => {
  console.error('âŒ Failed to start server:', err);
  process.exit(1);
});

